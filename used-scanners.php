  <script>
    const minRange = document.getElementById('min');
    const maxRange = document.getElementById('max');
    const products = document.querySelectorAll('.product-card');
    const brandBoxes = document.querySelectorAll('.brand-box');
    const modelBoxes = document.querySelectorAll('.model');
    const minValueDisplay = document.getElementById('min-value');
    const maxValueDisplay = document.getElementById('max-value');

    let selectedBrands = new Set();
    let selectedModels = new Set();

    function formatPrice(value) {
      return `₹${value} Lakh`;
    }

    function updateTrack() {
      let min = parseInt(minRange.value);
      let max = parseInt(maxRange.value);


      if (min >= max) {
        min = max - 1;
        minRange.value = min;
      }
      if (max <= min) {
        max = min + 1;
        maxRange.value = max;
      }


      const percent1 = (min / 50) * 100;
      const percent2 = (max / 50) * 100;


      document.getElementById('track').style.background =
        `linear-gradient(to right, #ccc ${percent1}%, orange ${percent1}%, orange ${percent2}%, #ccc ${percent2}%)`;

      minValueDisplay.textContent = formatPrice(min);
      maxValueDisplay.textContent = formatPrice(max);
    }

    function applyFilters() {
      const minPrice = parseInt(minRange.value) * 100000;
      const maxPrice = parseInt(maxRange.value) * 100000;

      products.forEach(product => {
        const brand = product.dataset.brand.toLowerCase();
        const model = product.dataset.model.toLowerCase();
        const price = parseInt(product.dataset.price);

        const matchesBrand = selectedBrands.size === 0 || selectedBrands.has(brand);
        const matchesModel = selectedModels.size === 0 || selectedModels.has(model);
        const matchesPrice = price >= minPrice && price <= maxPrice;

        const productCol = product.closest('.col');
        productCol.style.display = (matchesBrand && matchesModel && matchesPrice) ? 'block' : 'none';
      });
    }

    brandBoxes.forEach(box => {
      box.addEventListener('click', () => {
        const brand = box.dataset.brand.toLowerCase();
        box.classList.toggle('active');
        selectedBrands.has(brand) ? selectedBrands.delete(brand) : selectedBrands.add(brand);
        applyFilters();
      });
    });

    modelBoxes.forEach(box => {
      box.addEventListener('click', () => {
        const model = box.dataset.model.toLowerCase();
        box.classList.toggle('active');
        selectedModels.has(model) ? selectedModels.delete(model) : selectedModels.add(model);
        applyFilters();
      });
    });

    minRange.addEventListener('input', () => {
      updateTrack();
      applyFilters();
    });

    maxRange.addEventListener('input', () => {
      updateTrack();
      applyFilters();
    });

    // Initialize on page load
    updateTrack();
    applyFilters();
  </script>

    

