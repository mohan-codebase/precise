<script>
function validateStep(stepClass, nextBtnClass) {
    const step = document.querySelector(stepClass);
    const nextBtn = document.querySelector(nextBtnClass);

    function checkFields() {
        const requiredFields = step.querySelectorAll("input[required]");
        let isValid = true;

        requiredFields.forEach(field => {
            if (field.type === "radio") {
                const radioGroup = step.querySelectorAll(`input[name="${field.name}"]`);
                const oneChecked = [...radioGroup].some(r => r.checked);
                if (!oneChecked) isValid = false;
            } else if (field.type === "file") {
                if (field.files.length === 0) isValid = false;
            } else {
                if (!field.value.trim()) isValid = false;
            }
        });

        nextBtn.disabled = !isValid;
    }

    step.addEventListener("input", checkFields);
    step.addEventListener("change", checkFields);

    nextBtn.disabled = true;
}

