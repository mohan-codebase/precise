    <script>
      document.addEventListener('DOMContentLoaded', function() {
        const rentBox = document.querySelector('.rent-box');
        const subsBox = document.querySelector('.subs-box');
        const radios = document.querySelectorAll('input[name="rent-sub"]');

        function toggleBoxes() {
          const selected = document.querySelector('input[name="rent-sub"]:checked');

          // First hide both smoothly
          rentBox.classList.remove('show');
          subsBox.classList.remove('show');

          if (selected) {
            if (selected.value === '3D Scanner Rental') {
              rentBox.classList.add('show');
            } else if (selected.value === '3D Scanner Subscription') {
              subsBox.classList.add('show');
            }
          }
        }

        radios.forEach(radio => {
          radio.addEventListener('change', toggleBoxes);
        });

        // Make sure both are hidden at start
        rentBox.classList.remove('show');
        subsBox.classList.remove('show');
      });
    </script>

    <script>
      document.addEventListener('DOMContentLoaded', function () {
        const form1 = document.querySelector('.form1');
        const nextBtn = document.querySelector('.next-btn');

        const rentalRadio = document.getElementById('radio1');
        const subscriptionRadio = document.getElementById('radio2');

        const rentSelect = document.querySelector('select[name="rent-date"]');
        const subYearSelect = document.querySelector('select[name="sub-year"]');
        const subMonthSelect = document.querySelector('select[name="sub-Monts"]');

        // Disable the next button initially
        nextBtn.disabled = true;

        function updateRequiredFields() {
          if (rentalRadio.checked) {
            rentSelect.required = true;
            subYearSelect.required = false;
            subMonthSelect.required = false;
          } else if (subscriptionRadio.checked) {
            rentSelect.required = false;
            subYearSelect.required = true;
            subMonthSelect.required = true;
          }
          validateForm1();
        }

        function validateForm1() {
          const requiredFields = form1.querySelectorAll('input[required], select[required]');
          let allFilled = true;

          requiredFields.forEach(field => {
            if (field.type === "radio") {
              const name = field.name;
              const group = form1.querySelectorAll(`input[name="${name}"]`);
              const isChecked = Array.from(group).some(input => input.checked);
              if (!isChecked) {
                allFilled = false;
              }
            } else if (field.tagName.toLowerCase() === 'select') {
              if (field.value === "" || field.selectedIndex === 0) {
                allFilled = false;
              }
            } else {
              if (!field.value.trim()) {
                allFilled = false;
              }
            }
          });

          nextBtn.disabled = !allFilled;
        }

        rentalRadio.addEventListener('change', updateRequiredFields);
        subscriptionRadio.addEventListener('change', updateRequiredFields);

        // Watch for any input or change in form1
        form1.addEventListener('input', validateForm1);
        form1.addEventListener('change', validateForm1);

        updateRequiredFields(); // Initial setup
      });
    </script>

