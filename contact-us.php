            const x = 20 + i * 20;
            const y = 30 + Math.random() * 10;
            const angle = Math.random() * 0.4 - 0.2;
            ctx.save();
            ctx.translate(x, y);
            ctx.rotate(angle);
            ctx.fillText(generatedCaptcha[i], 0, 0);
            ctx.restore();
        }

        // Add lines for more complexity
        for (let i = 0; i < 4; i++) {
            ctx.beginPath();
            ctx.moveTo(Math.random() * canvas.width, Math.random() * canvas.height);
            ctx.lineTo(Math.random() * canvas.width, Math.random() * canvas.height);
            ctx.strokeStyle = "#ccc";
            ctx.stroke();
        }
    }

    // Validate before form submission
    document.getElementById("contact-form").addEventListener("submit", function (e) {
        const userCaptcha = document.getElementById("captchaInput").value.trim();
        if (userCaptcha.toUpperCase() !== generatedCaptcha.toUpperCase()) {
            e.preventDefault();
            alert("Incorrect CAPTCHA. Please try again.");
            generateCaptcha(); // refresh CAPTCHA
        }
    });

    // Refresh CAPTCHA when the button is clicked
    document.getElementById("refreshCaptchaBtn").addEventListener("click", function () {
        generateCaptcha(); // refresh CAPTCHA
        document.getElementById("captchaInput").value = ''; // Clear input field
    });

