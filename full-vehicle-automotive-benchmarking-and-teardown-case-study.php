            <h2 class="sec-head">Objective</h2>
            <p>The main objective of the project is to compare and analyse the NVH analysis of the BIW along with the cross-members. We analyse and compare the physical vibration test results and develop a definite element model to match actual test results. This will be used as a reference to run ideal tests using stimulation techniques. To achieve the final output we use 3D scanning, reverse engineering, vibration testing, CAE analysis, material testing and obtaining data regarding the mechanical properties of each part after material testing.</p>

