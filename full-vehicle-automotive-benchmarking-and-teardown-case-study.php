                <li>Drafting Bill of Materials at each step of the teardown to correspond sub assembly modelling & Assembly documentation, Material composition analysis & Grade identification, Suggestion of alternate materials, Correlation of Grade physical properties with actual measured properties on critical parts.</li>
            </ul>

