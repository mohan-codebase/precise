  <script>
    document.addEventListener('DOMContentLoaded', function () {
  const buttons = document.querySelectorAll('.know-btn');

  // Hide all target sections initially
  buttons.forEach(btn => {
    const targetSelector = btn.getAttribute('data-target');
    const targetElement = document.querySelector(targetSelector);
    if (targetElement) {
      targetElement.style.display = 'none';
    }
  });

  buttons.forEach(btn => {
    btn.addEventListener('click', function () {
      const targetSelector = this.getAttribute('data-target');
      const targetElement = document.querySelector(targetSelector);
      
      if (targetElement) {
        // Check if this section is already open
        const isOpening = targetElement.style.display === 'none' || !targetElement.style.display;
        
        // Close all sections first
        buttons.forEach(otherBtn => {
          const otherTarget = document.querySelector(otherBtn.getAttribute('data-target'));
          if (otherTarget) {
            otherTarget.style.display = 'none';
            otherBtn.innerHTML = 'Suitable Application <i class="fa-solid fa-circle-chevron-up"></i>';
            otherBtn.classList.remove('active');
          }
        });
        
        // Now handle the clicked section
        if (isOpening) {
          // Show the section
          targetElement.style.display = 'block';
          this.innerHTML = 'Show Less <i class="fa-solid fa-circle-chevron-down"></i>';
          this.classList.add('active');
          
          // Smooth scroll to the section
          setTimeout(() => {
            const yOffset = -120;
            const y = targetElement.getBoundingClientRect().top + window.pageYOffset + yOffset;
            window.scrollTo({ top: y, behavior: 'smooth' });
          }, 100);
        }
      }
    });
  });
});
