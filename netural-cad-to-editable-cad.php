                            <h4>Overview</h4>
                            <div class="overview-cont">
                                <h6>Neutral CAD</h6>
                                <p>
                                    Neutral  CAD formats are designed to be universal. One of the advantages of a neutral CAD is that it allows the chance to share 3D information between companies and collaborators and the information is kept safe. Neutral CAD formats often have missing geometry or design history features. Neutral CAD files were developed to import and export between different software packages. The files will be imported and modifications can start with existing geometry in the file. Very often, these are the only files available on obsolete parts. Neutral  CAD is available in  .step, .stl, .iges, .x_t file formats.
                                </p>
                                <h6>Neutral CAD to Editable CAD</h6>
                                <P>
                                    Reverse engineered neutral CAD model formats such as STEP / IGES can be edited in native CAD software. If a neutral CAD format is uploaded in reverse engineering software, a mesh is created. We can take that mesh as a reference and we can live-transfer it to CAD software. It will generate the model tree automatically. We can add or subtract features and also edit geometric features in the CAD model. In the case of Hybrid 3D CAD models, which have both surface features as well as solid features, geometric features such as holes, fillets, ribs, etc. may be edited easily but complex surface features may not be editable.
                                </P>
