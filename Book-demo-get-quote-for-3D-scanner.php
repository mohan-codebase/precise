<script>
  let captchaText = "";

  function rand(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
  }

  function generateCaptcha() {
    const canvas = document.getElementById("captchaCanvas");
    const ctx = canvas.getContext("2d");
    ctx.clearRect(0, 0, canvas.width, canvas.height);

    captchaText = Math.random().toString(36).substring(2, 8); // Mixed case

    ctx.fillStyle = "#f2f2f2";
    ctx.fillRect(0, 0, canvas.width, canvas.height);

    for (let i = 0; i < captchaText.length; i++) {
      const fontSize = rand(18, 26);
      const angle = rand(-25, 25) * Math.PI / 180;
      const x = 10 + i * 18;
      const y = rand(25, 35);
      ctx.save();
      ctx.translate(x, y);
      ctx.rotate(angle);
      ctx.font = `${fontSize}px Arial`;
      ctx.fillStyle = `rgb(${rand(0,150)}, ${rand(0,150)}, ${rand(0,150)})`;
      ctx.fillText(captchaText[i], 0, 0);
      ctx.restore();
    }

    document.getElementById("captchaInput").value = "";
    document.getElementById("captchaError").textContent = "";
    document.getElementById("submit-btn").disabled = true;
  }

  function validateFormInputs() {
    const name = document.getElementById("name").value.trim();
    const email = document.getElementById("email").value.trim();
    const phone = document.getElementById("phone").value.trim();
    const company = document.querySelector("input[name='company']").value.trim();
    const address = document.getElementById("address").value.trim();
    const country = document.getElementById("country1").value;
    const state = document.getElementById("state1").value;
    const city = document.getElementById("city1").value;
    const captchaInput = document.getElementById("captchaInput").value.trim();

    const emailValid = /^[^ ]+@[^ ]+\.[a-z]{2,3}$/.test(email);
    const phoneValid = /^\d{10}$/.test(phone);
    const captchaValid = captchaInput === captchaText;

    const allValid =
      name &&
      emailValid &&
      phoneValid &&
      company &&
      address &&
      country !== "Select Country" &&
      state !== "Select State" &&
      city !== "Select City" &&
      captchaValid;

    // Show captcha error only if entered
    document.getElementById("captchaError").textContent =
      captchaInput && !captchaValid ? "CAPTCHA does not match (case-sensitive)" : "";

    document.getElementById("submit-btn").disabled = !allValid;
  }

  document.addEventListener("DOMContentLoaded", () => {
    generateCaptcha();

    const inputs = document.querySelectorAll("#form_1 input, #form_1 select");
    inputs.forEach(input => {
      input.addEventListener("input", validateFormInputs);
      input.addEventListener("change", validateFormInputs);
    });
  });
