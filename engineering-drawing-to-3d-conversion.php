                            <h4>Overview</h4>
                            <div class="overview-cont">
                                <h6>Engineering Drawing</h6>
                                <p>
                                    An engineering drawing is a type of technical drawing used to define the requirements for engineering products or components.  It describes the process of making the item,  used to convey engineering ideas during the design process, or provides a record of an existing item. 2D engineering drawing is still largely used for the production of schematics. MEP Drawings are shop drawings that are a method used for the installation of mechanical, electrical, and plumbing building services. These drawings are developed in 3D using Building Information Modelling. The models are delivered to the customer as an IGES and STEP file.
                                </p>
                                <h6>Engineering Drawing to 3D Conversion</h6>
                                <p>
                                    Engineering drawing to 3D CAD conversion is the process of converting 2D Technical drawings into 3D CAD Models. 2D drawings are in the x and y-axis where a 3D CAD Model has the additional z-axis.  First, input 2D  drawings are studied along with tooling capacity. Our team began the development of 3D CAD models based on the 2D files step by step using reverse engineering software. Final assembly model, fabrication drawings along with BOMs, and exploded isometric views of the assembly were shared with the customer. Comprehensive 3D CAD models with all detailed information help the customer in a better manner with reduced turnaround time. The CAD conversion establishes a digital platform for all the designs to facilitate design changes in the future.
                                </p>
