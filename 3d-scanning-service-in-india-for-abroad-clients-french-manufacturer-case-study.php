            <p>Thanks to industrial handheld 3D laser scanners that offer simplistic and unparalleled 3D scanning of small-sized as well as large-sized components, capturing all details effectively. Precise3DM’s Handheld Metrology 3D scanning solution using the <strong>FreeScan X7</strong> 3D scanner was a perfect match for such parts.</p>

            <p>It can scan the tiniest details of the part with shear accuracy, featuring an accuracy level of 0.03mm. With such accuracy, it can even scan a single human hair. It features a flexible and convenient scanning mode for small and large objects with complex shapes. FreeScan features blue laser light which is helpful in capturing data of cylindrical bore depth where light is difficult to reach.</p>

            <p>Geomagic Control X is a complete metrology-grade, quality control software equipped with powerful tools designed to improve multiple existing workflows.</p>

            <h2 class="sec-head">Workflow</h2>
            <p>We first set up the FreeScan X7 and ensured calibration was performed to update the scanner’s accuracy. After calibration, the process of parametric adjustment was performed. The FreeScan scanner automatically adjusts laser intensity according to the surrounding light.</p>

            <p>Before starting, scanning markers were applied to the part. These markers work as reference points. Each marker has a unique code that the scanner reads to create an image and register scans. 3D scanners have limitations with reflective, black, or transparent surfaces. To ensure the best capture, a developer layer was sprayed on the part to reduce reflectivity.</p>

            <p>The whole part was scanned within 2 hours. A digital image (point cloud data) was created. This was subsequently converted into a mesh model (triangulation of points) and saved in .stl format for import into Geomagic Control X.</p>

            <h2 class="sec-head">Inspection with Geomagic Control X</h2>
            <p>The customer already had a CAD object for reference. Both the original CAD and the 3D scanned file were imported into CX. Using a specific alignment technique tailored to the customer's needs, we superimposed both models.</p>

            <p>We performed a 3D comparison analysis showing the extent of wheel wear against the original design. This allowed us to extract many 3D dimensions, such as the wheel diameter and the distance between wheels. The final dimensional report was generated as a PDF and sent to the manufacturer in France.</p>

