            <p>Sculfort has been recognized as a renowned manufacturer and with a vibrant history in the expertise of lathes and other heavy machineries for over 100 years. They manufacture wheel boring and install them in metros. In today’s digital world, measuring part dimensions and checking accuracy with the conventional method is a tedious job. Scanning the part with a 3D scanner, creating a digital image, and comparing it with the CAD model is the future of Quality Assurance and Quality Control processes.</p>

            <p>Sculfort has manufactured a wheel boring and installed it in Mumbai Metro. After the successful installation and the first run, they wanted to check the shape and dimensions. We see how we made it possible through our in-house products and capabilities.</p>

            <h2 class="sec-head">Challenges</h2>
            <p>The customer wants to check the periodic wear in the rail wheels as well as measure the dimensions of numerous features. The diameter of the wheels was measured by the customer, and it was discovered that one wheel was more offset than the other. To get more insight data on the wheel dimensions, the customer wanted more accurate measurements. It was impossible to measure the diameter of the wheel with a conventional method, so we had to come up with an advanced solution.</p>

            <h2 class="sec-head">Solution</h2>
