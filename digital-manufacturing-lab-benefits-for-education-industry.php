            <p>Education is a journey which teaches us the art of living, not just the livelihood. Educational Institutions must provide opportunities to each and every individual to learn through experience and develop critical thinking and problem solving skills as Educational Institutions are the nexus between industry and students.</p>

            <p>The ushering in of Industry 4.0 has proliferated the growth of Digital Manufacturing technologies across diverse industry verticals. In the wake of this demand from industry, most of India’s premier institutions have set-up Digital Manufacturing Labs (DML) to inculcate critical thinking and problem solving skills through experiential learning. They are like a playground for aspiring engineers, architects and product designers.</p>

