      let imageIndex = 0;
      const totalImages = images.length;

      // A function that updates the carousel display to show the specified image
      const slideImage = () => {
         if (imageIndex >= totalImages - 1) {
            imageIndex = totalImages - 1; // Stop at the last image
         } else if (imageIndex <= 0) {
            imageIndex = 0; // Stop at the first image
         }
         carousel.style.transition = "transform 0.5s ease-in-out";
         carousel.style.transform = `translateX(-${imageIndex * 100}%)`;
      };

      // A function that updates the carousel display to show the next or previous image
      const updateClick = (e) => {
         const direction = e.target.id === "next" ? 1 : -1;
         if ((imageIndex === 0 && direction === -1) || (imageIndex === totalImages - 1 && direction === 1)) {
            return; // Prevent movement beyond first and last images
         }
         imageIndex += direction;
         slideImage();
      };

      // Add event listeners to the navigation buttons
      buttons.forEach((button) => button.addEventListener("click", updateClick));

