            <h2 class="sec-head">Workflow process</h2>
            <p>The customer has already given us the part and the CAD data. Our aim is to scan the part using a 3D scanner and compare the scan data with the given CAD file and prepare the digital inspection report. This is a medium size object so we can use GOM ATOS 3D scanner with non-contact structured blue light technology.</p>

            <ul>
                <li>Clean the pillion holder part before spraying</li>
                <li>Apply to spray on the part to be scanned by Gom Atos scanner</li>
                <li>Place the target points stickers on the part to be scanned as a reference point. With the help of Gom Atos compact 3D scanner, scan the pillion holder part.</li>
                <li>Start the mesh conversion and post processing of the part. Convert this data into .stl format</li>
                <li>Import the scan data and CAD data in the Geomagic Control X reverse engineering software</li>
                <li>Use initial alignment feature to align scan and CAD data</li>
                <li>Use the 3D compare feature to compare scan data against CAD data by applying respective tolerance value</li>
                <li>Use comparison point feature to compare the deviation and color map bar</li>
                <li>Capture some views for the report. Use multiple 2D compare features to create multiple sections for 2D and compare the 2D feature for deviations.</li>
                <li>Use linear dimension feature to compare the deviation in dimension</li>
                <li>Finally, the inspection report is generated in the pdf format.</li>
            </ul>

