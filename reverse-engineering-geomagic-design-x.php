      document.addEventListener("DOMContentLoaded", () => {
         const checkboxes = document.querySelectorAll(".checkbox-div input[type='checkbox']");

         // Function to toggle visibility of hidden rows
         const toggleRows = (checkbox) => {
            const parentRow = checkbox.closest("tr");
            if (!parentRow) return;

            let nextRow = parentRow.nextElementSibling;

            while (nextRow && nextRow.classList.contains("hide-tr")) {
               nextRow.style.display = checkbox.checked ? "table-row" : "none";
               nextRow = nextRow.nextElementSibling;
            }
         };

         // Add event listeners and apply initial state
         checkboxes.forEach((checkbox, index) => {
            // Toggle rows visibility based on the initial state of the checkbox
            toggleRows(checkbox);

            // Add event listener for changes
            checkbox.addEventListener("change", function () {
               toggleRows(this);
            });

            // Ensure the first checkbox is checked and open its rows
            if (index === 0) {
               checkbox.checked = true;
               toggleRows(checkbox);
            }
         });
      });
   </script>


