      document.addEventListener('DOMContentLoaded', () => {
         // Event delegation: select the container, not individual images
         const carousel = document.querySelector('#carouselExampleFade');

         carousel.addEventListener('click', (event) => {
            if (event.target.classList.contains('tab2-image')) {
               const clickedImage = event.target;

               // Find the closest main image within the current carousel-item
               const activeCarouselItem = clickedImage.closest('.carousel-item');
               const mainImage = activeCarouselItem.querySelector('.mainImage');

               if (mainImage) {
                  // Apply fade-in effect
                  mainImage.classList.add('fade-in');

                  // Change the src after the fade-out effect duration
                  setTimeout(() => {
                     mainImage.src = clickedImage.src;

                     // Wait for the new image to load before removing fade-in class
                     mainImage.onload = () => {
                        mainImage.classList.remove('fade-in');
                     };
                  }, 500);  // Match this with CSS transition duration
               }
            }
         });
      });
