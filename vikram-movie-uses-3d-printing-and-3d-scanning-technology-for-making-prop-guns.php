            <p>Subsequently, we used the <strong>Figure 4 Industrial 3D Printer</strong> to print the digital replica of the gun. Figure 4 standalone is an industrial 3D printer typically used in the engineering and healthcare industry for direct end-use production of plastic, rubber and biocompatible parts without injection moulds.</p>

            <p>But we used figure 4 standalone 3D printer for movie application as the production house wanted an excellent surface finish and texture of the gun as per the original one. We 3D scanned and printed it in a short time, and the output came out very well, as expected by the production house. As you can witness in the movie, it is being used in most of the Vikram movie action scenes by many movie casts.</p>

