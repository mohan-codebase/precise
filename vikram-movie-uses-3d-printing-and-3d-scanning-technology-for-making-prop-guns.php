            <h2 class="sec-head">Vikram Movie</h2>
            <p><strong>3D scanning and 3D printing technology used in the Vikram movie scenes</strong></p>

            <p>Have you heard about the tragedy that people used a prop gun in the movie and caused an accidental death in the shooting? Yes, Producer and actor Alec Baldwin fired a prop gun that killed one crew member and injured another on the set of the movie “Rust” in New Mexico very recently. In this blog, we want to explain an actual case study about the latest Pan India film, Vikram -2022 movie and how 3D printing technology can prevent such incidents by 3D printing the non-functional gun for the movie scenes.</p>

            <p>Actor Kamal Haasan is known for his interest in bringing new technology into the Movie industry. The Tamil movie “Vikram -1986” was the first movie to use a computer to record songs in Kollywood, and in fact, it was the first movie to show even the computer on the screen to the audience.</p>

            <p>As another example, Alavandhan 2001 was the first film in India to use a motion control rig to capture the movement of pictures. Also, this was the first film in India to use animation sequences, and the list goes on. We are not discussing here all those lists of movies how kamal Haasan influenced technology in his films.</p>

            <p>Vikram -2022 is one of the multi-language Pan Indian action thriller films has hit the screens on June 3rd across India in Tamil, Malayalam, Hindi and Kannada. We Precise3DM have used advanced technology such as 3D scanning, reverse engineering and 3D printing technology to make the Prop guns in Vikram movie scenes.</p>

            <p>As per the storyline of Vikram movie, kamal Hasan used to handle different types of guns in the film. It was hard for the movie crew to manufacture or procure different types of original guns, so they contacted Precise3DM to explore the possibility of duplicating the original one with the dummy guns.</p>

            <p>We have used Structured light 3D scanning technology SolutioniX C500 to 3D scan the original gun so we could make a digital replica of the same. 3D scan data was later used as a reference in <a href="geomagic-design-x-features-in-india-2026.php">Geomagic Design X</a> to reverse engineer into the format that we could 3D print.</p>

