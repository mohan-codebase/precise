            <p class="lead font-weight-bold">Inspecting large parts is always mind-boggling!</p>
            <p>At first, it may appear to be a straightforward scanning task. But it can quickly turn into a multi-phase project that stretches out into weeks! Let’s look into the challenges and solutions in handheld 3D scanning of large parts.</p>

            <h2 class="sec-head">Challenge:</h2>
            <p>The customer uses a tactile measurement system to measure large-sized and heavy-weighted casting parts for the quality control process but left other details untested because the casting component is hard to move. We cannot go for an optical structured scanner or CMM because it is time-consuming. The quality measurements of parts cannot be effectively controlled, leading to considerable deviations in harsh environments.</p>

            <h2 class="sec-head">Solution:</h2>
            <p>Thanks to an industrial 3D handheld laser Scanner that offers simplistic and unparalleled 3d scanning of large-sized components, it captures all details of the components effectively. It is nothing but PRECISE3DM’s handheld metrology 3D scanning solution from <strong>shining3D Freescan UE 7</strong> 3D scanner, a perfect option for large-sized casting parts.</p>
            <p>It’s a metrology-grade certified handheld portable type laser scanner. It features a flexible and convenient scanning mode to scan large measurement objects with complex shapes. The accuracy ranges up to 0.03mm. Geomagic control X comprehensive 3D inspection and digital measurement software helps to extract any advanced measurement on a 3D scanned file.</p>

            <h2 class="sec-head">3D Scanning & 3D inspection Workflow:</h2>
            <p>Blue and white light 3D Scanners use structure light 3D Scanning technology in which the projector projects the fringe pattern to the surface. It limits the scanning field of view for 3D scanning the large objects. We prefer FreeScan X7 as it uses the laser as a light source and it is not harmful to the eyes. It can work in harsh environments and generates as-built raw data.</p>

            <ul>
                <li>Applying the white spray to the pump casing part for recognition by the scanner is not required in the FreeScan X7.</li>
                <li>For the volume of a part to be scanned, GD&T inspection has to be done to verify its flatness</li>
                <li>Die preparation before the scanning.</li>
                <li>FreeScan X7 works with a Sticker based method. The reference for Scanning large parts is taken by placing target stickers on them.</li>
                <li>FreeScan X7 is used to scan the pump casing part in the shopfloor now.</li>
                <li>Mesh conversion is done and the output data is stored in .stl format</li>
                <li>3D inspection is done in Geomagic Control X software using the Coordinate Alignment method.</li>
                <li>3D and 2D measurement of the part is done in Geomagic Control X software</li>
                <li>GD&T surface measuring of the part is done in Geomagic Control X software.</li>
                <li>Views have been driven and the first statistical inspection report has been generated.</li>
            </ul>

            <h2 class="sec-head">Hardware used</h2>
            <p><strong>FreeScan X7</strong> is an industrial-grade 3D Laser scanner system with only 0.95 kg following Laser class II eye-safe certified & German VDI/VDE 2634 calibration.</p>

