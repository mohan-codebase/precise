    <section>
        <div class="container" style="margin-top:60px;">
            <h2 class="sec-head">Points that software uses for feature extraction</h2>
            <p>CAD Model Creation and Editing: Once the surface reconstruction is complete, Geomagic Design X allows you to create a CAD model based on the scanned data. The software provides a range of powerful tools for creating and editing CAD features such as curves, surfaces, and solids. You can refine the model’s geometry, add details, and make design modifications as needed. Geomagic Design X’s intuitive interface and robust feature set make CAD modeling a seamless and efficient process.</p>
        </div>
    </section>

    <section>
        <div class="container" style="margin-top:60px;">
            <h2 class="sec-head">Estimating the right radius</h2>
            <p>Geomagic Design X offers various types of fillet options that automatically calculate the radius from the CAD edge. It displays a suggested radius that the user can adjust according to their preference. The user can choose to either stick with the suggested value or set a specific rounded value.</p>
            <img src="assets/images/blog13/3d-rev13.png" class="mb-4" style="width:100%" alt="Estimating the right radius">
            <p>Fillet option in Geomagic Design X</p>
