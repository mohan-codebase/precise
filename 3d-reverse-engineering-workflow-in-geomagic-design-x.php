                    <li>Neutral CAD – Dump Solids</li>
                </ul>
            </div>
            <p>Once you are satisfied with the CAD model, Geomagic Design X allows you to export it in various industry-standard formats, such as STEP or IGES, for further utilisation. The exported model can be seamlessly integrated into CAD/CAM systems or used for downstream processes such as 3D printing, manufacturing, or simulation.</p>
            <img src="assets/images/blog13/3d-rev14.png" class="mb-4" style="width:100%" alt="Exporting the CAD model">
            <p>Dump solid file export options inside the software</p>
        </div>
    </section>

    <section>
        <div class="container" style="margin-top:60px;">
            <h2 class="sec-head">Live transfer to CAD</h2>
            <p>After you finish your parametric modelling in Geomagic Design X, all those parametric features you’ve created will be seamlessly moved over to your preferred CAD software such as SOLIDWORKS, UNIGRAPHICS, CREO, etc… This has a significant advantage: it lets you bring the entire modelling history within your CAD software, In other words, you can have the complete design history in your CAD software even if you haven’t modeled it there initially. This is made possible through the Design X live transfer function. This live transfer function’s key benefit is to save time and effort while ensuring the creation of highly accurate design features from the 3D scan.</p>
            <img src="assets/images/blog13/3d-rev13.png" class="mb-4" style="width:100%" alt="Estimating the right radius">
