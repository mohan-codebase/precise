        <div class="container" style="margin-top:60px;">
            <h2 class="sec-head">CAD model generation</h2>
            <p>Geomagic Design X offers a comprehensive range of CAD modeling features, empowering users to create various forms from sketches. These include options such as exclusion, revolve, sweep, and loft, enabling users to achieve their desired design outcomes.Here in this case study we extruded the body using the initial sketch we have extracted.</p>
            <img src="assets/images/blog13/3d-rev8.png" class="mb-4" style="width:100%" alt="2D Sketching">

            <h2 class="sec-head" style="margin-top:40px;">Powerful Surface Creation</h2>
            <p>Geomagic Design X possesses the capability to generate complex surfaces from the scanned region. Users can employ either the mesh fitting technique or the automated extraction of loft surfaces to create these surfaces. This versatile functionality allows for the creation of intricate and diverse surface geometries to meet specific design requirements.</p>
            <p>We have selected the freeform region so software can extract the 3D mesh fit surface.</p>
            <img src="assets/images/blog13/3d-rev9.png" class="mb-4" style="width:100%" alt="Surface Creation">
            <p>Mesh Fit function In Geomagic Design X</p>
        </div>
    </section>

