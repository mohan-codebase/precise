            <h2 class="sec-head">Boolean operations :</h2>
            <p>In Geomagic Design X, users have the ability to carry out Boolean operations. In this particular example we have used a surface as a tool and a solid as a targeted body. Moreover, you can create a variety of Boolean operations like cut, merge, split, and unite for both surface and solid entities within the Design X environment.</p>
            <img src="assets/images/blog13/3d-rev10.png" class="mb-4" style="width:100%" alt="Boolean operations">

            <h2 class="sec-head" style="margin-top:40px;">Extracting CAD primitives (Surface & Solids features )</h2>
            <p>Wizard functions are among the powerful features in Geomagic Design that enable the automatic extraction of various CAD features from segmented scan regions. These functions offer a convenient and efficient way to extract and incorporate different types of CAD elements, enhancing the overall design process.</p>
            <img src="assets/images/blog13/3d-rev11.png" class="mb-4" style="width:100%" alt="Extracting CAD primitives">
        </div>
    </section>

