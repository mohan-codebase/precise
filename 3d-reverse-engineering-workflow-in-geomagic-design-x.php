        <div class="container" style="margin-top:60px;">
            <h1 class="sec-head">3D Scan Directly or Import the 3D scan data in Geomagic Design X :</h1>
            <div class="text-center">
                <img src="assets/images/blog13/3d-rev2.png" class="mb-4" style="width:100%; border-radius: 16px;" alt="3D Reverse Engineering Workflow in Geomagic Design X">
