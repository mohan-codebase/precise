            </div>

            <h2 class="sec-head" style="margin-top:40px;">Create 2D As built & Design Intent sketch from Mesh file</h2>
            <p>Users have the freedom to utilise any of the system planes or select specific faces from the mesh file in order to generate cross sections of the mesh file for creating both as-built and design intent sketches. The as-built sketch accurately represents the cross section as a reference, disregarding any manufacturing errors. On the other hand, the design intent sketch creates a sketch with geometric constraints, capturing the intended design specifications. </p>
            <img src="assets/images/blog13/3d-rev7.png" class="mb-4" style="width:100%" alt="2D Sketching">
        </div>
