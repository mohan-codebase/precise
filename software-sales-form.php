  <script>
    const demoBox = document.querySelector('.demo-box');
    const downloadBox = document.querySelector('.download-box');
    const scheduleBox = document.querySelector('.schedule-box');
    const gstBox = document.querySelector('.gst-div');

    const downloadCheckbox = document.getElementById('download');
    const demoCheckbox = document.getElementById('demo');
    const quoteCheckbox = document.getElementById('quote');

    function setRequiredInBox(box, required) {
      const inputs = box.querySelectorAll('input');
      inputs.forEach(input => {
        if (required) {
          input.setAttribute('required', 'required');
        } else {
          input.removeAttribute('required');
        }
      });
    }

    function updateVisibility() {
      // Hide all boxes
      demoBox.style.display = 'none';
      downloadBox.style.display = 'none';
      scheduleBox.style.display = 'none';
      gstBox.style.display = 'none';

      // Clear required fields
      setRequiredInBox(demoBox, false);
      setRequiredInBox(downloadBox, false);
      setRequiredInBox(scheduleBox, false);

      // Handle Download
      if (downloadCheckbox.checked) {
        downloadBox.style.display = 'block';
        setRequiredInBox(downloadBox, true);

        demoCheckbox.checked = false;
        quoteCheckbox.checked = false;
      }
      // Handle Demo
      else if (demoCheckbox.checked) {
        scheduleBox.style.display = 'block';
        setRequiredInBox(scheduleBox, true);

        downloadCheckbox.checked = false;
        quoteCheckbox.checked = false;
      }
      // Handle Quote
      else if (quoteCheckbox.checked) {
        demoBox.style.display = 'block';
        gstBox.style.display = 'block';
        setRequiredInBox(demoBox, true);

        downloadCheckbox.checked = false;
        demoCheckbox.checked = false;
      }
    }

    // Attach listeners
    [downloadCheckbox, demoCheckbox, quoteCheckbox].forEach(checkbox => {
      checkbox.addEventListener('change', updateVisibility);
    });

    // Initialize on load (optional)
    window.addEventListener('DOMContentLoaded', updateVisibility);
  </script>

