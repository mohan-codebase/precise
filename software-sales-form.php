<script>
  let generatedCaptcha = "";

  // Function to generate the CAPTCHA
  function generateCaptcha() {
    const canvas = document.getElementById("captchaCanvas");
    const ctx = canvas.getContext("2d");
    
    // Clear the canvas before drawing new CAPTCHA
    ctx.clearRect(0, 0, canvas.width, canvas.height);

    // Define the possible characters
    const chars = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";
    generatedCaptcha = "";
    
    // Generate a random 5-character CAPTCHA
    for (let i = 0; i < 5; i++) {
      generatedCaptcha += chars.charAt(Math.floor(Math.random() * chars.length));
    }

    // Draw background
    ctx.fillStyle = "#f2f2f2";  // Light gray background
    ctx.fillRect(0, 0, canvas.width, canvas.height);

    // Draw CAPTCHA characters
    ctx.font = "25px Arial";
    ctx.fillStyle = "#000";  // Black color for text
    for (let i = 0; i < generatedCaptcha.length; i++) {
      const x = 20 + i * 25; // Adjust the position for each character
      const y = 30 + Math.random() * 10; // Add random vertical displacement
      const angle = Math.random() * 0.4 - 0.2;  // Add slight rotation
      ctx.save();
      ctx.translate(x, y);
      ctx.rotate(angle);
      ctx.fillText(generatedCaptcha[i], 0, 0);  // Draw the character
      ctx.restore();
    }

    // Draw some noise lines for complexity
    for (let i = 0; i < 4; i++) {
      ctx.beginPath();
      ctx.moveTo(Math.random() * canvas.width, Math.random() * canvas.height);
      ctx.lineTo(Math.random() * canvas.width, Math.random() * canvas.height);
      ctx.strokeStyle = "#ccc";
      ctx.stroke();
    }
  }

  // Validate CAPTCHA before form submission
  function validateCaptcha() {
    const userCaptcha = document.getElementById("captchaInput").value.trim();
    const submitButton = document.getElementById("submit");

    // Check if the CAPTCHA is correct
    if (userCaptcha.toUpperCase() === generatedCaptcha.toUpperCase()) {
      submitButton.disabled = false;  // Enable the submit button
    } else {
      submitButton.disabled = true;   // Keep the submit button disabled
    }
  }

  // Refresh CAPTCHA when the button is clicked
  document.getElementById("refreshCaptchaBtn").addEventListener("click", function () {
    generateCaptcha(); // Regenerate CAPTCHA
    document.getElementById("captchaInput").value = ''; // Clear the input field
    document.getElementById("submit").disabled = true;  // Keep the submit button disabled
  });

  // Event listener for the CAPTCHA input to validate
  document.getElementById("captchaInput").addEventListener("input", validateCaptcha);

  // Generate CAPTCHA on page load
  window.onload = function () {
    generateCaptcha(); // Generate CAPTCHA when the page loads
  };
</script>

