<!-- country state and city selection -->

  <script>
    function loadCountries(countrySelect, stateSelect, citySelect) {
      if (!countrySelect || !stateSelect || !citySelect) return;

      let allCountriesData = [];

      // Fetch countries and their states
      fetch('https://countriesnow.space/api/v0.1/countries/states')
        .then(response => response.json())
        .then(res => {
          if (!res.error) {
            allCountriesData = res.data;
            countrySelect.innerHTML = '<option value="" selected>Select Country</option>';
            allCountriesData.forEach(country => {
              var option = document.createElement('option');
              option.value = country.name; // Use country name
              option.textContent = country.name;
              countrySelect.appendChild(option);
            });
          }
        })
        .catch(error => console.error('Error loading countries:', error));

      countrySelect.addEventListener('change', function () {
        stateSelect.disabled = true;
        citySelect.disabled = true;
        stateSelect.innerHTML = '<option value="" selected>Select State</option>';
        citySelect.innerHTML = '<option value="" selected>Select City</option>';

        var selectedCountryName = this.value;
        if (selectedCountryName) {
          var countryData = allCountriesData.find(c => c.name === selectedCountryName);
          if (countryData && countryData.states && countryData.states.length > 0) {
            stateSelect.innerHTML = '<option value="" selected>Select State</option>';
            countryData.states.forEach(state => {
              var option = document.createElement('option');
              option.value = state.name; 
              option.textContent = state.name;
              stateSelect.appendChild(option);
            });
            stateSelect.disabled = false;
          }
        }
      });

      stateSelect.addEventListener('change', function () {
        citySelect.disabled = true;
        citySelect.innerHTML = '<option value="" selected>Select City</option>';

        var selectedCountryName = countrySelect.value;
        var selectedStateName = this.value;

        if (selectedCountryName && selectedStateName) {
          fetch('https://countriesnow.space/api/v0.1/countries/state/cities', {
            method: 'POST',
            headers: {
              'Content-Type': 'application/json'
            },
            body: JSON.stringify({
              country: selectedCountryName,
              state: selectedStateName
            })
          })
            .then(response => response.json())
            .then(res => {
              if (!res.error && res.data) {
                citySelect.innerHTML = '<option value="" selected>Select City</option>';
                res.data.forEach(cityName => {
                  var option = document.createElement('option');
                  option.value = cityName; 
                  option.textContent = cityName;
                  citySelect.appendChild(option);
                });
                citySelect.disabled = false;
              }
            })
            .catch(error => console.error('Error loading cities:', error));
        }
      });
    }

    // Load for country-state-city
    var countrySelect1 = document.getElementById('country1');
    var stateSelect1 = document.getElementById('state1');
    var citySelect1 = document.getElementById('city1');

    if (countrySelect1 && stateSelect1 && citySelect1) {
      loadCountries(countrySelect1, stateSelect1, citySelect1);
    }
  </script>


