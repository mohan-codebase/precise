            <h2 class="sec-head">Challenge</h2>
            <p>In the automotive industry, we do own or purchase a part. But if there is a need for the same part again we cannot purchase it because it leads to more budget, time consumption, and disruption in workflow. A customer came to PRECISE3DM with a Car wheel Rim. He cannot purchase it again because it is imported from a foreign country and he wants to manufacture it in India. So our challenge is that we want to reverse engineer it without any modification and need the CAD file format for mass production in our country.</p>

            <h2 class="sec-head">Solution</h2>
            <p>This process requires an As-built model and the only solution is using 3D reverse engineering <strong>Geomagic Design X</strong> software. Geomagic Design X is the industry-leading reverse engineering software that uses 3D scan data captured from a 3D scanner to create history-based CAD. Geomagic Design X is purpose-built to create manufacturing-ready CAD models directly from scan data. With Geomagic Design X, you can create feature-based, editable solid models with a history tree compatible with your existing CAD software. Geomagic Design X is a tremendous benefit for reverse engineering process.</p>
            
            <div class="text-center">
                <img src="assets/images/blog43/img2.png" alt="Scanning car wheel rim with Blue Light technology" class="content-img">
            </div>

            <h2 class="sec-head">Workflow process</h2>
            <p>For 3D scanning of small to medium size objects, we prefer optical blue light <strong>GOM ATOS compact scan</strong>. It can create digital copies of parts. The blue light technology further improves the scanning of dark or colored surfaces while making them resilient to environmental factors.</p>

            <ul>
                <li>Clean the glossy portion of the rim before spraying</li>
                <li>Apply spray on the part to be scanned by Gom Atos scanner</li>
                <li>Place the target points stickers on the part to be scanned as a reference point.</li>
                <li>With the help of Gom Atos compact 3D scanner, scan the car wheel rim.</li>
                <li>After the scanning is completed, post-processing work takes place to correct the uneven surface in the software.</li>
                <li>The 3D scan data is imported in <strong>Geomagic Design X</strong> software</li>
                <li>Initially, the scan data is aligned using the feature Align between scan data.</li>
            </ul>

            <div class="text-center">
                <img src="assets/images/blog43/img3.png" alt="Mesh Segmentation and Feature Extraction in Design X" class="content-img">
            </div>

            <ul>
                <li>We can use the decimate feature to triangulate high definition point cloud into mesh data. The model region of the mesh is extracted using the Auto Segment feature.</li>
                <li>The CAD data can be created from the mesh surface and it is compared with the scan data</li>
            </ul>

            <div class="text-center">
                <img src="assets/images/blog43/img4.png" alt="CAD vs Scan 3D Comparison" class="content-img">
            </div>

            <p>The output CAD format is given in .iges, .setp file, or other desirable formats to the customer.</p>

