                var $faqItem = $(this).closest('.faq-item');
                var $answerWrap = $faqItem.find('.faq-answer-wrapper');
                
                if ($faqItem.hasClass('active')) {
                    $faqItem.removeClass('active');
                    $answerWrap.slideUp(300);
                } else {
                    // Close all others
                    $('.faq-item').removeClass('active');
                    $('.faq-answer-wrapper').slideUp(300);
                    
                    // Open this one
                    $faqItem.addClass('active');
                    $answerWrap.slideDown(300);
                }
            });
        });
    </script>
