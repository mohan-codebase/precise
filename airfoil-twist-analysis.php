                                <h6>Airfoil Twist Analysis </h6>
                                <p>Turbine engines have hundreds of airfoils, consisting of complex arrays, and are inspected at an average of one airfoil an hour using a conventional Coordinate Measuring Machine (CMM).Utilizing a Handheld 3D scanner and a 3D inspection software creates an associated mesh that can be compared to nominal CAD for critical feature deviation and reported within minutes. A simple surface deviation color map can indicate quick imperfections from the trailing edge of an airfoil. Initially, 3D scan data and CAD data are imported into 3D inspection software. Alignment is made and choose the 3D compare feature for base plane and uses Airfoil feature for adjusting color map and coordinate system. Again we need to select the 2D twist feature for analyzing the blade portion with primary and secondary constraints. A custom report can be created to provide an automated reporting workflow with desired CAD output.</p>
                                
