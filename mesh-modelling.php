                            <h4>Overview</h4>
                            <div class="overview-cont">
                                <h6>Mesh Modeling</h6>
                                <p>Mesh modeling is a vector representation of an irregular or organically shaped object. We can convert point-clouds or images into a mesh. As a result, from the mesh we can generate a surface created by triangles. Mesh modeling is a time-saving alternative compared to creating traditional CAD models. Stl files are usually meshes that can be changed, but the tools and processes are different. Any physically existing objects can be scanned with the help of a 3D scanner and the scan data is imported in 3D reverse engineering software like Geomagic Design X. Initially, the coordinates are aligned using Auto-segmentation features in all regions. The reference planes are defined and a mesh setup feature is selected. We can use surface and solid tools to define the mesh feature by extruding the model. Finally, using simple sketch features like chamfer or fillet, we can complete the CAD model and give it to the customer in the desired format.</p>
