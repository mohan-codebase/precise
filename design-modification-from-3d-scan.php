                            <h4>Overview</h4>
                            <div class="overview-cont">
                                <h6>Design Modification</h6>
                                <p>In the process of using an existing manufactured part for reverse engineering a missing part or creating a CAD model from an imperfect prototype, we need to make some changes to the design before 3D printing. Design modification is the process of adding or editing the parametric features of any natural objects or existing objects like diameter, alignment, cross-section, etc. The mating parts can also be scanned and incorporated into the design, resulting in parts that fit better consistently. By utilizing reverse engineering, new designs can incorporate and improve upon engineering optimization already inherent to the manufactured part. These modified design parts fit up to or integrate with existing objects.
                                </p>
                                <h6>Design Modification from 3D scan </h6>
                                <p>To obtain design modification from the 3D Scan, an existing part i.e, physical objects are scanned and 3D data is obtained. Import the scan data in the reverse engineering software and alter the design according to the customer's requirement. Create a matching design to the existing object. Combine and position the data we created. Using the parametric features refine and finalize the design. The output can be delivered in desirable CAD format to the customer.
                                </p>
