      function generateCaptcha() {
        const chars = "ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnpqrstuvwxyz23456789";
        let captcha = "";
        const captchaBox = document.getElementById("captcha-box");
        captchaBox.innerHTML = ""; // Clear previous CAPTCHA

        for (let i = 0; i < 6; i++) {
          const char = chars.charAt(Math.floor(Math.random() * chars.length));
          captcha += char;

          const span = document.createElement("span");
          span.textContent = char;
          span.style.transform = `rotate(${Math.floor(Math.random() * 20 - 10)}deg) skewX(${Math.floor(Math.random() * 10 - 5)}deg)`;
          span.style.fontSize = `${Math.floor(Math.random() * 8 + 20)}px`;
          span.style.color = getRandomColor();
          span.style.fontWeight = "bold";
          span.style.display = "inline-block";

          captchaBox.appendChild(span);
        }

        document.getElementById("captcha-hidden").value = captcha;
      }

      function getRandomColor() {
        const colors = ["#333", "#444", "#555", "#0066cc", "#cc0000", "#009933", "#990099"];
        return colors[Math.floor(Math.random() * colors.length)];
      }

      function validateCaptcha() {
        const input = document.getElementById("captcha-input").value.trim();
        const actual = document.getElementById("captcha-hidden").value.trim();
        if (input !== actual) {
          alert("Incorrect CAPTCHA. Please try again.");
          generateCaptcha();
          return false;
        }
        return true;
      }

      window.onload = generateCaptcha;
