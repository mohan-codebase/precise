     <script>
        const downloadModal = document.getElementById('downloadModal');
        const downloadForm = document.getElementById('downloadForm');
        const downloadBtn = document.getElementById('downloadBtn');
        const originalText = downloadBtn.textContent;

        // When modal opens → set image + file link
        downloadModal.addEventListener('show.bs.modal', function (event) {
            const button = event.relatedTarget;
            const image = button.getAttribute('data-img');
            const downloadLink = button.getAttribute('data-download');

            document.getElementById('modalImage').src = image;
            document.getElementById('modalDownloadLink').value = downloadLink;
        });

        // When modal closes → reset form + button
        downloadModal.addEventListener('hidden.bs.modal', function () {
            downloadForm.reset();
            downloadBtn.disabled = true;
            downloadBtn.textContent = originalText;
        });

        // Enable submit button only when all fields have value
        downloadForm.addEventListener('input', function () {
            const name = document.getElementById('name').value.trim();
            const email = document.getElementById('email').value.trim();
            const phone = document.getElementById('phone').value.trim();
            downloadBtn.disabled = !(name && email && phone);
        });

        // Handle form submit
        downloadForm.addEventListener('submit', function (e) {
            e.preventDefault();

            // Disable button + change text
            downloadBtn.disabled = true;
            downloadBtn.textContent = 'Processing...';

            const fileUrl = document.getElementById('modalDownloadLink').value;

            // Send form data to PHP
            fetch(this.action, {
            method: 'POST',
            body: new FormData(this)
            })
            .then(response => {
            if (response.ok) {
                // Trigger file download
                const link = document.createElement('a');
                link.href = fileUrl;
                link.download = '';
                document.body.appendChild(link);
                link.click();
                document.body.removeChild(link);

                // Close modal
                const modalInstance = bootstrap.Modal.getInstance(downloadModal);
                modalInstance.hide();

                // Redirect after short delay
                setTimeout(() => {
                window.location.href = 'thankyou-software.php';
                }, 1000);
            } else {
                alert("Something went wrong while submitting the form.");
                downloadBtn.disabled = false;
                downloadBtn.textContent = originalText;
            }
            })
            .catch(err => {
            console.error(err);
            alert("Error connecting to the server.");
            downloadBtn.disabled = false;
            downloadBtn.textContent = originalText;
            });
        });
      </script>
