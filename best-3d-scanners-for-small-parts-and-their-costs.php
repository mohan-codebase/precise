            <h2 class="sec-head">Problems in scanning small size parts (less than 500mm)</h2>
            <p>There are two problems in scanning small parts:</p>

            <h3 class="h3-sec-head">1. Feature size vs scanner resolution</h3>
            <p>These parts often have fine edges, thin walls, holes, threads, and micro-features. So,  if the scanner’s resolution and point spacing are not high enough, these details get smoothed out or missed entirely, leading to incomplete or inaccurate models.</p>
            <p>Consider a small precision-machined connector with a 0.6 mm hole and M1.2 threads. If the 3D scanner has a resolution of 0.3 mm, only a few data points will fall inside the hole or along the thread profile. So, the hole may appear oval or partially closed, and the threads may look rounded or completely washed out in the final mesh.</p>

            <h3>Tight tolerances</h3>
            <p>Many small parts are manufactured with tolerances in the micron range, so tight tolerances make errors less forgiving. Small parts are designed for precise fits, so parts and features, like shafts, holes, threads, and mating surfaces, are designed to work within extremely narrow limits. </p>
            <p>A few microns of deviation can decide whether a part fits, functions, or passes inspection. This is why small-part scanning requires scanners with high accuracy, fine point spacing, stable tracking, and controlled scanning conditions.</p>
