        <div class="container" style="margin-top:60px;">
            <h2 class="sec-head">Mesh Models</h2>
            <p>Mesh models are generated by converting point cloud data into a surface mesh composed of triangles or other polygonal shapes. These mesh models are commonly utilised for visualisation, analysis, and 3D printing purposes. However, it is important to note that mesh models are not ideal for design modifications or direct manufacturing.</p>
            <p>Typically mesh models are exported as .STL files. Our 3D scanners directly deliver Mesh models in .stl file format.</p>
            
            <div class="d-flex flex-wrap justify-content-between my-4">
                <div>
                    <h4 class="mt-4">Use Cases:</h4>
                    <ul class="use-cases-list">
                        <li>Import into RE software for CAD modeling purposes.</li>
                        <li>Use it with freeform or Digital Sculpting design software to enhance and modify organic shapes.</li>
                        <li>Applications in 3D inspection and facilitation of 3D printing processes.</li>
                        <li>Scanning and exporting of wood materials as mesh files, supporting wood carving manufacturing.</li>
                        <li>Use the enclosed mesh model for accurate volume calculations across various domains.</li>
                    </ul>
