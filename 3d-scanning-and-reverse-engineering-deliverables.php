        <div class="container" style="margin-top:60px;">
            <h1 class="sec-head">PointCloud</h1>
            <p>Point cloud data is a common output of laser and LIDAR scanning techniques. It consists of a collection of XYZ coordinates that represent the spatial positions of points on a physical object. These points collectively form a detailed and accurate representation of the object’s shape and structure in a digital format.</p>
            <p>Portable arms, LIDAR scanners, and DRONE-based lidar scanners produce the direct point cloud output that can be exported as .ASC, .Obj and other file formats.</p>
            

            <h4 class="mt-4">Use Cases:</h4>
            <ul class="use-cases-list">
                <li>Point clouds are directly used for 3D reverse engineering for manufacturing.</li>
                <li>Point clouds can be used to make Factory CAD modeling & scan to BIM modeling.</li>
                <li>Use 3D point cloud files for 3D inspection in metrology software.</li>
                <li>Point clouds assist in reconstructing crime scenes and take measurements for investigations and legal proceedings.</li>
                <li>Point clouds assist in studying landforms, geological features, and terrain changes over time.</li>
            </ul>

