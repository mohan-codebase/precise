                                <h6>2D/3D Curvature comparison</h6>
                                <p>Curvature quantifies the deviation between a curve and a straight line, or between a surface and a plane in 3D. The curvature of a 2D curve at a point P equals the inverse of the osculating circle radius r at P. Control the quality of turbines,  or blades with the included airfoil analysis tools. A 3D inspection software automates the measurement of Airfoil profiles, Leading and trailing edge position & thickness. Initially, the reference scan data is imported into the software. From the Airfoil feature,  we can pick and position the dimension of the derived CAD data for our comparison. The construction data is traced and saved for future reference. Using the "compare'' feature the derived and the reference CAD data are compared in 2D twist analysis. We can change the color mapping and visibility options for both references and derived data. A detailed report of the comparison is obtained and given to customers in the desired output formats.</p>
                                
