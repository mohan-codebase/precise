            <p>As depicted in the image above, the positive comparative values show excess material and negative values show a high shortage of material. The upper and lower tolerance limits specified by the client are +5mm and -5mm in thickness. The green colour represents deviations within the tolerance specified by the client. Red and orange show excess material beyond the higher tolerance limit and light blue and dark blue show a shortage of material beyond the lower tolerance limit.</p>

            <p>The image above represents a visual display of deviations. Geomagic Control X can also display the deviations in tabular format for your convenience. The inspection report can also be exported as PDF. Check out other blogs on <a href="geomagic-software-in-india-2026.php">Geomagic Control X</a> and understand the capabilities of the powerful inspection software.</p>

