            <p>Ever wondered what raw materials are employed to make private buses safe and reliable for inter-state travel? Do you think it is aluminium or iron? You are partially correct! Most of the bus parts are made out of composite materials like FRP (fibre reinforced polymer) as they possess superior properties to other metals. They are extremely versatile, lightweight, and efficient and hence, result in lighter, robust and durable manufactured parts.</p>

            <p>A composite tooling manufacturer based out of Andhra Pradesh; India approached us to streamline the inspection process of the Front & Rear Fascia of private buses being manufactured by them. Their team was inspecting the manufactured parts manually which led to judgemental errors. Based on their judgement they were deciding whether more composite material needs to be added to the part or remove from the part. Since buses are not small, one can imagine the size of the front and rear fascia of these buses. Each part was approximately 4 metres long and 3 metres wide. Manual methods and conventional tools made the inspection process of these huge parts time-consuming and laborious. Plus, a lot of re-work had to be done on the defective parts.</p>

            <p>Therefore, our client was hunting for a solution that can help them in comparing the manufactured part with their CAD data quickly and effectively. It is crucial to maintain the uniform width of the front and rear fascia of private buses as it may impact passengers negatively in case there is an accident.</p>

            <p>After an initial consultation and an online demo, the team was determined to go ahead with the 3D Scanning Solution provided by Precise3DM. The solution involved Precise3DM’s team visiting their manufacturing facility with an appropriate 3D Scanner and related accessories but integral to the solution was the 3D Scanner – <strong>FreeScan UE7</strong>.</p>

            <h2 class="sec-head">What is FreeScan UE7?</h2>
            <p>It is an easy-to-use and lightweight new-generation handheld 3D Scanner developed to provide a high-precision (0.02mm) digital inspection experience. It projects blue-coloured Class 2M (eye-safe) laser light on the objects to be scanned and captures data of the surface in the form of a Point Cloud. Businesses can trust the 3D Scan output of this 3D scanner as it exhibits very stable repeatability with a scan speed of 6,5,000 points/sec. One can acquire the scan output in the file formats – OBJ, STL, ASC, PLY, P3, 3MF.</p>
            <p>It supports the scanning of black and reflective surfaces to accommodate a wide range of scanning applications. It weighs just 670 grams and works on 12V. This 3D scanner model is approved by CE, FCC, ROHS, WEEE and KC. It is highly suitable for industries like Automotive, Transportation, Aerospace industry, Molding inspection, Energy generation, Machinery manufacturing, etc.</p>
