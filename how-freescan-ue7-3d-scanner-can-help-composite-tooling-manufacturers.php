            <h2 class="sec-head">Workflow</h2>
            <ul>
                <li><strong>Step 1:</strong> Calibrate the 3D Scanner</li>
                <li><strong>Step 2:</strong> Place the part to be scanned on a platform that makes it easy to scan. (You can even 3D scan the bigger parts when they are in assembled condition as well.)</li>
                <li><strong>Step 3:</strong> Stick markers on the surface of the part to ensure a high-quality scan output. All markers are allotted a unique reference ID. Since the surface is not transparent, developer spray is not required to be sprayed on the surface and scanning is done in normal mode.</li>
                <li><strong>Step 4:</strong> Scan the entire surface of the part in a single go.</li>
                <li><strong>Step 5:</strong> Register the Point Cloud and convert it into an .STL data file</li>
                <li><strong>Step 6:</strong> Import the .STL data file into the dedicated-inspection software Geomagic Control X and align it to the 3D CAD model.</li>
            </ul>

