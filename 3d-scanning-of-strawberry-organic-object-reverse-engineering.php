            <p>This is an honest and inspiring story about an organic object with a complicated surface topology getting scanned. In recent times, 3D scanning barges into the food industry and we are tiring to bring every organic object to this realism. Let’s look into the challenges and solutions in 3D scanning a strawberry.</p>

            <h2 class="sec-head">Challenge:</h2>
            <p><strong>Can you extract the details from the fruit and convert it to chocolate?</strong></p>
            <p>This is what a customer asked us. Now, this is a tough cookie. The customer approached us with the idea of creating chocolate in the shape of the fruit. We know how challenging it is to scan an organic object with a freeform shape. It’s a lot of work and we want to do it from scratch.</p>

            <h2 class="sec-head">Solution:</h2>
            <p>Normally, this kind of challenge would test the 3D Scanner in terms of how it would perform when scanning a small-sized object like a strawberry. The fine details of strawberry-like crevices and seeds push the 3D Scanner to scan up to its maximum capabilities.</p>
            <p>This is where our PRECISE3DM’s <strong>Einscan SP</strong> comes into play. Einscan SP is a most affordable desktop 3D Scanner designed to capture small to medium size objects. It is based on structured light technology. The Auto Scan mode uses the rotating turntable. It is versatile, fast, and optimizes scan time. Einscan SP provides a high accuracy of 0.05mm for a single scan. It has the great power of reconstructing the geometry of the object with fine details.</p>

            <h2 class="sec-head">Workflow process:</h2>
            <p>Our ultimate aim is to take the strawberry, scan it and prepare the mold cavity of the outer layer of the fruit. You can pour the chocolate into the mold and get the strawberry-shaped chocolate. We used Einscan SP 3D Scanner for this as it uses white light as a light source and it’s harmless.</p>
            <ul>
                <li>Our team selected a box of strawberry and isolated one of the nest strawberries from the box</li>
                <li>Using Einscan SP Scanner we scanned the exocarp (outer layer) of the strawberry.</li>
                <li>The time needed for the 360 deg auto scan is 1 min.</li>
                <li>After the scanning process, the mold should be created. For that surface of the strawberry should be in solid form</li>
                <li>We created a NURBS surface in <strong>Geomagic Design X</strong> software where the mesh is created and closed.</li>
                <li>It is now given as Dumpsolid format and the solid is created</li>
                <li>Now a this is extruded and split into half where the outer layer of the strawberry is created and now it is formed as a mold</li>
                <li>In this mold, the chocolate can be poured and get in the shape of a strawberry.</li>
            </ul>

