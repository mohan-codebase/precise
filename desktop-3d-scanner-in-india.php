   <script>
      const container = document.querySelector('.containerr');
      const next = document.querySelector('.next');
      const prev = document.querySelector('.prev');
      const boxes = document.querySelectorAll('.container-box'); // Select all the boxes
      const size = boxes[0].clientWidth + 30; // Calculate the size of one box, including margin
      let counter = 2; // Start with the third box as the active one

      // Center the active box and show adjacent boxes
      function centerActiveBox() {
         if (window.innerWidth <= 768) {
            // Mobile view: only one item in the center
            container.style.transform = `translateX(${-size * counter}px)`;
            boxes.forEach((box) => box.classList.remove('active'));
            boxes[counter].classList.add('active');
         } else {
            // Desktop view: show adjacent boxes
            const containerCenterOffset = (window.innerWidth / 2.24) - (size / 2);
            container.style.transform = `translateX(${containerCenterOffset - size * counter}px)`;

            // Reset all classes
            boxes.forEach((box) => box.classList.remove('active', 'prev', 'next'));

            // Set active, previous, and next classes
            boxes[counter].classList.add('active');
            if (counter > 0) {
               boxes[counter - 1].classList.add('prev'); // Previous box
            }
            if (counter < boxes.length - 1) {
               boxes[counter + 1].classList.add('next'); // Next box
            }
         }
      }

      // Update the active box
      function setActiveBox() {
         if (window.innerWidth > 768) {
            boxes.forEach((box, index) => {
               if (index === counter) {
                  box.classList.add('active');
               } else {
                  box.classList.remove('active');
               }
            });
         }
      }

      // Move to next box
      function nextElement() {
         counter = (counter + 1) % boxes.length; // Loop back to the first box when reaching the last
         setActiveBox();
         centerActiveBox();
      }

      // Move to previous box
      function prevElement() {
         counter = (counter - 1 + boxes.length) % boxes.length; // Loop back to the last box when reaching the first
         setActiveBox();
         centerActiveBox();
      }

      // Event listeners
      next.addEventListener('click', nextElement, false);
      prev.addEventListener('click', prevElement, false);

      // Initialize the third box as active and center it
      setActiveBox();
      centerActiveBox();

      // Adjust centering on window resize
      window.addEventListener('resize', centerActiveBox);


   </script>



